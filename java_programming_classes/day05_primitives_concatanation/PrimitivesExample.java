package java_programming_classes.day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){
        //**** PRIMITIVES *****
        byte byteType = 127; //or -128
        short shortValue = 32767; //or -32768
        int intValue = 2147483647; //or -2147483648
        long longValue = 52565362356235L; // add L/l at the end to show this is a long number
        //**** DECIMAL NUMBERS/FLOATING POINT TYPES *****
        float floatValue = 123.456f; //add F/f at the end
        double doubleValue = 123.45667;
        //**** CHARTER TYPE ****
        char charValue = 'a'; //single character (letters, special characters) in single quotes
        //**** BOOLEAN TYPE ****
        boolean booleanValue = true; //true or false in lowercase
        //PRINT VARIABLE VALUES
        System.out.println(byteType);
        System.out.println(shortValue);





    }
}
