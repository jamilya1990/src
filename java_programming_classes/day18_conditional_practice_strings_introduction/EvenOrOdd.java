package java_programming_classes.day18_conditional_practice_strings_introduction;

public class EvenOrOdd {
    public static void main(String[] args) {
        int num = 5;
        if (num % 2 == 0) {
            System.out.println(num + " is even");
        } else {
            System.out.println(num + " is odd");
        }
    }
}
